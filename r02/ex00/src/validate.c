int	validate(char *str)
{
	int i;
	int space;

	i = 0;
	space = 0;
	while (str[i] != 0)
	{
		if (!(str[i] >= '0' && str[i] <= '9') && str[i] != ' ')
			return (0);
		if (str[i] == ' ')
			space = 1;
		if (space == 1 && str[i] != ' ')
			return (0);
		i++;
	}
	return (1);
}
